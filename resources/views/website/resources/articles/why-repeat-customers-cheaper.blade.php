						<h2 class="m-b-30"><b>Customer Retention Leads to Lower Marketing Costs</b></h2>
						<p>According to the <a target="_blank" href="https://hbr.org/2014/10/the-value-of-keeping-the-right-customers">Harvard Business Review</a>,
							<p><i>Acquiring new customers for your business is anywhere from <b>5 to 25 times more costly</b> than retaining an existing one.</i>
							<p>Not just that. There’s a <a target="_blank" href="https://www.clickz.com/are-ecommerce-customer-retention-strategies-improving/105454/">60% to 70% success selling rate</a> to customers you already have, while 5% to 20% success selling rate to a new prospect.
							<p>But how, exactly, are you able when retaining customers than just acquiring new ones?
						</p>
						<h3 class="m-b-30"><b>Lead Generation Cost</b></h3>
						<p>In business, a <b>lead is someone who left some kind of contact information behind</b>.
							<p>Commonly, it’s in the following forms:
								<ul>
									<li>Email address
									<li>Phone number
									<li>Name and address
								</ul>
							<p>This type of information doesn’t come cheap. 
							<p>In fact, according to <a target="_blank" href="https://surveyanyplace.com/average-cost-per-lead-by-industry/">Survey Any Place</a>, the average cost per lead across industries is around <b>$33 to $34</b>.
						<div class="text-center">
							<img class="m-t-20 b-r-5" src="{{ url('images/assets/main/blog/Average-cost-per-lead-Survey-Anyplace.png') }}">
						</div>
							<p>That means you have to spend at least $33 to gain a new lead, not counting the success rate of selling to them.
							<p>But for customers who already bought something from you, there’s <i>no need to spend the same amount of money to reach them</i>.
							<p>Why? Because obviously, you already have their information! 
							<p>You can reach them whenever you want though, of course, you still have to follow the proper etiquette of marketing assuming you already gained their permission to use their email addresses and other forms of contact information.
						<h3 class="m-b-30"><b>Marketing Expenses</b></h3>
						<p>Marketing budgets vary from company to company. That’s because companies utilize different marketing schemes, tactics, and channels.
							<p>But for you to understand how enormous companies spend in marketing, the <a target="_blank" href="https://www.brafton.com/blog/content-marketing/the-ultimate-list-of-marketing-spend-statistics-for-2019-infographic/">US alone spent $197.47 billion</a> while China spent $79.08 billion on just advertising.
							<p>According to <a target="_blank" href="https://deloitte.wsj.com/cmo/2017/01/24/who-has-the-biggest-marketing-budgets/">Deloitte Digital’s data</a>, industries, on average, spend a good 11.61% of their budget on marketing.
							<p>Here are the specifics for each industry:
						<div class="text-center">
							<img class="m-t-20 b-r-5" src="{{ url('images/assets/main/blog/Marketing-Budget-1-GRAPHIC.png') }}">
						</div>
							<p>The percentages represent millions of dollars.
							<p>But the cost in marketing or advertising to repeat customers is lower. More often, all you need to do is send them an email about your new offers and products.
							<p>When it comes to repeat customers, there’s no need to spend on other mediums to reach them and market them. There are plenty of free channels you can use — <b>social media</b> and <b>email</b>.
							<p><i>The bottom line is:</i>
							<p><b>Focusing on existing customers lowers marketing expenses.</b>
							<p>You will spend less effort, money, and time finding and cultivating new prospects and convincing them that your business is the one they should avail from.
							<p>When a customer has availed services or shopped items from your business a few times, this certain person does not require more convincing to come back to your store unless they have a disappointing experience.
							<p>Since you have already established trust between your business and these repeat customers, you have inspired great confidence in your products and services. 
							<p>You may already know much information about them through repeated transactions, making it easier for you to identify and predict their needs and next moves.
						</p>
