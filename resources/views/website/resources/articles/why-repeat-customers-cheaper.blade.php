						<h2 class="m-b-30"><b>How about the revenue?</b></h2>
						<p>You might consider the research entitled <a target="_blank" href="https://www.bain.com/insights/prescription-for-cutting-costs-bain-brief/">“Prescription for Cutting Costs” by Bain & Company</a> that emphasizes loyal relationships into cost savings:
							<p><b>Increasing focus on customer retention by 5% can increase profits and sales from 25% to 95%.</b>
							<p>Through a recent retail survey by <a target="_blank" href="https://home.kpmg/xx/en/home.html">KPMG LLP’s Retail Industry</a>, here are the significant drivers of your business’ revenue:
						<div class="text-center">
							<img class="m-t-20 b-r-5" src="{{ url('images/assets/main/blog/recent-retail-industry.png') }}">
						</div>
							<p>As you can see, <b>52%</b> believed that <i>customer retention is the top significant driver of a company’s revenue growth in the next 1 to 3 years</i>.
							<p>These will help you grow revenue!
							<p>Here’s the caveat:
							<p><b>You need to constantly earn your customer’s loyalty.</b>
							<p>Because if you stop trying, your customers will feel that you no longer care about them. This is the main reason, according to <a target="_blank" href="https://www.superoffice.com/blog/customer-complaints-good-for-business/">SuperOffice</a>, why a customer abandons a business or company.
						<div class="text-center">
							<img class="m-t-20 b-r-5" src="{{ url('images/assets/main/blog/why-do-customers-leave-a-company.png') }}">
						</div>
							<p>Here are other benefits of customer retention:
						</p>
						<h3 class="m-b-30"><b>1. Repeat Customers are less sensitive to price shifts</b></h3>
						<p>Price stops being a <b>big issue</b> once a customer had <i>good experiences shopping products or availing services</i> from you. 
							<p>According to <a target="_blank" href="https://www.onlinemarketinginstitute.org/blog/2014/05/what-you-never-knew-about-repeat-customers-and-what-to-do-now/">John Dawes from the University of South Australia</a>, customers who have been shopping, dealing and transacting a certain company for a long period ( i.e., long-tenured group, repeat customers ) were <i>half as sensitive to a high change in price</i> as compared to newer customers.
						</p>
						<h3 class="m-b-30"><b>2. Repeat Customer Considerations</b></h3>
						<p>Loyal customers are <b>5 times as likely to forgive</b> when there are errors in their purchase.
							<p>Handling orders manually often leads to <i>inevitable human errors</i>, especially when you are stuck working with paper-based order forms. 
							<p>However, with loyal customers, they tend to <i>understand and consider these minor incidents</i>. This ensures you that if these errors happen, there would be a lesser problem.
						</p>
						<h3 class="m-b-30"><b>3. Brand Advocates through Customer Retention</b></h3>
						<p>Customer Retention is also a way of <b>promoting your brand or company</b>. 
							<p>Simply because it is human psychology — <i>when a person experiences something nice, he or she would share it with almost everyone around</i>. 
							<p>When your customers have experienced reliable service and quality products, <i>the likelihood of these people spreading the good word about your company can go up exponentially</i>. 
							<p>Know that satisfied customers will willingly share with their social circle about how their remarkable shopping experience effortlessly.
							<p>This is a marketing technique of your brand for free of cost!
						</p>
