						<h2 class="m-b-30"><b>Use Loyalty Systems More</b></h2>
						<p>Now that you have an idea of how advantageous it is to focus on customer retention, you should have a loyalty system in place.
							<p>A loyalty system or program is a <b>rewards program of a company for customers who are ‘loyal’</b>. <i>Loyal customers are those who willingly make frequent purchases as a result of great customer service and satisfaction.</i>
							<p>Do loyalty systems cost less than marketing and advertising for new customers?
							<p>This may sound a bit contradictory but even if you’re giving money in the form of a coupon, discount, or freebies, <i>loyalty systems definitely cost less</i>.
							<p>With loyalty programs, what you spend comes back right away in the form of repeat purchase or a referral! You only reward customers who took action which is also profitable for your business.
							<p>The action can be:
								<ul>
									<li>Purchase
									<li>Referral
									<li>Sharing to social media
									<li>And others
								</ul>
							<p>Suffice to say that <i>loyalty systems pay for themselves</i>.
							<p>If you don’t have a paid loyalty system in place, there are a few things you can do to earn a customer’s loyalty:
								<ul>
									<li>Stay in touch with your current customer base
									<li>Maintain a professional appearance in every way
									<li>Gather every actionable data point concerning the transactional and behavioral aspects of your customers
									<li>Take time to reach out to customers you have not talked to in years
									<li>Craft a loyalty program that rewards repeat customers
									<li>Let repeat customers be the first to know about upcoming events and seasonal tips
								</ul>
						</p>
