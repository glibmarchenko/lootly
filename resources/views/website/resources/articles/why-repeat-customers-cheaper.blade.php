							<p>The repeat business phenomenon happens when <b>customers tend to go back, again and again, to purchase products or avail services from your company</b>. This type of customer is called “repeat customers”.
							<p>But what can you do to gain repeat customers?
							<p><i>The answer is simple:</i>
							<p><b>Focus on customer retention.</b>
